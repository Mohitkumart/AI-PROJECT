import streamlit as st
import datetime
import requests

BASE_URL = "http://localhost:8000"  # Backend endpoint

st.set_page_config(
    page_title="Travel Planner Agentic Application",
    page_icon="ğŸ§³",
    layout="centered",
    initial_sidebar_state="expanded"
)

st.title("ğŸŒ Travel Planner Agentic Application")

# ğŸ§  Initialize chat history
if "messages" not in st.session_state:
    st.session_state.messages = []

# ğŸ§¼ Add "New Session" button to clear chat memory
if st.button("ğŸ†• Start New Session"):
    st.session_state.messages = []

# ğŸ—£ï¸ Show chat history so far
if st.session_state.messages:
    st.subheader("ğŸ§  Chat Memory:")
    for msg in st.session_state.messages:
        st.markdown(f"**{msg['role'].capitalize()}:** {msg['content']}")

# ğŸ’¬ Header prompt
st.header("How can I help you in planning a trip? Let me know where do you want to visit.")

# ğŸ“ User input form
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("Your Input", placeholder="e.g. Plan a trip to Goa for 5 days.")
    submit_button = st.form_submit_button("Send")

if submit_button and user_input.strip():
    try:
        # ğŸ§  Prepare payload with memory
        payload = {
            "question": user_input,
            "memory": st.session_state.messages
        }

        # â³ Show spinner while thinking
        with st.spinner("Thinking..."):
            response = requests.post(f"{BASE_URL}/querry", json=payload)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned.")

            # ğŸ§  Update memory
            st.session_state.messages.append({"role": "user", "content": user_input})
            st.session_state.messages.append({"role": "assistant", "content": answer})

            # ğŸ§¾ Show formatted response
            markdown_content = f"""
# âœˆï¸ AI Travel Plan
**ğŸ•’ Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}
**ğŸ¤– Created by:** Agentic AI Travel Agent

---

{answer}

---

*This travel plan was generated by AI. Please verify details like prices, availability, and schedules before booking.*
            """
            st.markdown(markdown_content)

        else:
            st.error("âŒ Failed to respond: " + response.text)

    except Exception as e:
        st.error(f"ğŸš¨ An error occurred: {e}")
